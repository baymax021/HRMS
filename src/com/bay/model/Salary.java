package com.bay.model;

public class Salary {
  private String s_id;
  private String employee_id;
  private String basic_salary;
  private String merit_pay;
  private String social_security;
  private String call_back_pay;
  private String reward_punish_id;

  public String getS_id() {
    return s_id;
  }

  public void setS_id(String s_id) {
    this.s_id = s_id;
  }

  public String getEmployee_id() {
    return employee_id;
  }

  public void setEmployee_id(String employee_id) {
    this.employee_id = employee_id;
  }

  public String getBasic_salary() {
    return basic_salary;
  }

  public void setBasic_salary(String basic_salary) {
    this.basic_salary = basic_salary;
  }

  public String getMerit_pay() {
    return merit_pay;
  }

  public void setMerit_pay(String merit_pay) {
    this.merit_pay = merit_pay;
  }

  public String getSocial_security() {
    return social_security;
  }

  public void setSocial_security(String social_security) {
    this.social_security = social_security;
  }

  public String getCall_back_pay() {
    return call_back_pay;
  }

  public void setCall_back_pay(String call_back_pay) {
    this.call_back_pay = call_back_pay;
  }

  public String getReward_punish_id() {
    return reward_punish_id;
  }

  public void setReward_punish_id(String reward_punish_id) {
    this.reward_punish_id = reward_punish_id;
  }
}
